package vo;

import java.io.Serializable;

public class TicketVO implements Serializable{
	private static final long serialVersionUID = 8965908516791994700L;
	
	private String ticket_id;
	private String ticket_kinds;
	private int ticket_pri;
	public String getTicket_id() {
		return ticket_id;
	}
	public void setTicket_id(String ticket_id) {
		this.ticket_id = ticket_id;
	}
	public String getTicket_kinds() {
		return ticket_kinds;
	}
	public void setTicket_kinds(String ticket_kinds) {
		this.ticket_kinds = ticket_kinds;
	}
	public int getTicket_pri() {
		return ticket_pri;
	}
	public void setTicket_pri(int ticket_pri) {
		this.ticket_pri = ticket_pri;
	}
	
	
}
