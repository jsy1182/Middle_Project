package vo;

import java.io.Serializable;

public class ReserveVO implements Serializable{
	private static final long serialVersionUID = 708101528528892403L;
	
	private String res_id;
	private String res_time;
	private String ride_id;
	private String mem_id;
	private String ride_name;

	public String getRes_id() {
		return res_id;
	}
	public void setRes_id(String res_id) {
		this.res_id = res_id;
	}
	public String getRes_time() {
		return res_time;
	}
	public void setRes_time(String res_time) {
		this.res_time = res_time;
	}
	public String getRide_id() {
		return ride_id;
	}
	public void setRide_id(String ride_id) {
		this.ride_id = ride_id;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	
	public String getRide_name() {
		return ride_name;
	}
	public void setRide_name(String ride_name) {
		this.ride_name = ride_name;
	}
	
}
