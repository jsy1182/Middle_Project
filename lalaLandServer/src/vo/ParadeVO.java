package vo;

import java.io.Serializable;

public class ParadeVO implements Serializable{
	private static final long serialVersionUID = 3768444552386026139L;
	
	private String parade_id;
	private String parade_name;
	private String parade_time;
	private String parade_pos;
	private String parade_yes;
	public String getParade_id() {
		return parade_id;
	}
	public void setParade_no(String parade_id) {
		this.parade_id = parade_id;
	}
	public String getParade_name() {
		return parade_name;
	}
	public void setParade_name(String parade_name) {
		this.parade_name = parade_name;
	}
	public String getParade_time() {
		return parade_time;
	}
	public void setParade_time(String parade_time) {
		this.parade_time = parade_time;
	}
	public String getParade_pos() {
		return parade_pos;
	}
	public void setParade_pos(String parade_pos) {
		this.parade_pos = parade_pos;
	}
	public String getParade_yes() {
		return parade_yes;
	}
	public void setParade_yes(String parade_yes) {
		this.parade_yes = parade_yes;
	}
	
	
}
