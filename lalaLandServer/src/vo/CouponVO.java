package vo;

import java.io.Serializable;
import java.sql.Date;

public class CouponVO implements Serializable{
	private static final long serialVersionUID = -6495958947100433836L;
	
	private String ej_id;
	private String event_title;
	private double event_disc;
	private Date event_start;
	private Date event_end;
	
	public String getEj_id() {
		return ej_id;
	}
	public void setEj_id(String ej_id) {
		this.ej_id = ej_id;
	}
	public String getEvent_title() {
		return event_title;
	}
	public void setEvent_title(String event_title) {
		this.event_title = event_title;
	}
	public double getEvent_disc() {
		return event_disc;
	}
	public void setEvent_disc(double event_disc) {
		this.event_disc = event_disc;
	}
	public Date getEvent_start() {
		return event_start;
	}
	public void setEvent_start(Date event_start) {
		this.event_start = event_start;
	}
	public Date getEvent_end() {
		return event_end;
	}
	public void setEvent_end(Date event_end) {
		this.event_end = event_end;
	}
	
}
